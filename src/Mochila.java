
public class Mochila {
	private int bagGold;
    private int bagDiamond;
    private int bagAxe;
    private int bagKey;
    private int bagPot;
	
    public int getDiamante() {
		return bagDiamond;
	}
	
	public void setDiamond(int diamond) {
		this.bagDiamond = diamond;
	}
	
	public int getOuro() {
		return bagGold;
	}
	
	public void setOuro(int gold) {
		this.bagGold = gold;
	}

	public int getBagAxe() {
		return bagAxe;
	}

	public void setBagAxe(int bagAxe) {
		this.bagAxe = bagAxe;
	}
	
	public int getBagKey() {
		return bagKey;
	}

	public void setBagKey(int bagKey) {
		this.bagKey = bagKey;
	}
	
	public int getBagPot() {
		return bagPot;
	}

	public void setBagPot(int bagPot) {
		this.bagPot = bagPot;
	}
    
}
